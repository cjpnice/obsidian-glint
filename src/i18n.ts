export type L10nKey = keyof typeof L10N.zh;

export function translate(language: AppLanguage, key: L10nKey, values: Record<string, string | number> = {}): string {
  let text: string = L10N[language][key] ?? L10N.en[key] ?? key;
  for (const [name, value] of Object.entries(values)) {
    text = text.split(`{{${name}}}`).join(String(value));
  }
  return text;
}
